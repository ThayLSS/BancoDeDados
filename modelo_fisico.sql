ADD COLUMN kmAtual INT DEFAULT NULL;


CREATE TABLE IF NOT EXISTS RESERVA (
  IDRESERVA INT NOT NULL,
  DATAINICIOPREV DATE NOT NULL,
  DATAFIMPREV DATE NOT NULL,
  IDFILIAL_DEVOLUCAO INT NOT NULL,
  IDFILIAL_RETIRADA INT NOT NULL,
  CLIENTE_IDCLIENTE INT NOT NULL,
  VEICULO_IDVEICULO INT NOT NULL,
  PRIMARY KEY (IDRESERVA, IDFILIAL_DEVOLUCAO, IDFILIAL_RETIRADA, CLIENTE_IDCLIENTE, VEICULO_IDVEICULO),
  INDEX fk_RESERVA_FILIAL1_idx (IDFILIAL_DEVOLUCAO ASC) VISIBLE,
  INDEX fk_RESERVA_CLIENTE1_idx (CLIENTE_IDCLIENTE ASC) VISIBLE,
  INDEX fk_RESERVA_VEICULO1_idx (VEICULO_IDVEICULO ASC, IDFILIAL_RETIRADA ASC) VISIBLE,
  CONSTRAINT fk_RESERVA_FILIAL1
    FOREIGN KEY (IDFILIAL_DEVOLUCAO)
    REFERENCES LOCADORA_DE_CARRO.FILIAL (IDFILIAL)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  CONSTRAINT fk_RESERVA_CLIENTE1
    FOREIGN KEY (CLIENTE_IDCLIENTE)
    REFERENCES LOCADORA_DE_CARRO.CLIENTE (IDCLIENTE)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  CONSTRAINT fk_RESERVA_VEICULO1
    FOREIGN KEY (VEICULO_IDVEICULO , IDFILIAL_RETIRADA)
    REFERENCES LOCADORA_DE_CARRO.VEICULO (IDVEICULO , FILIAL_IDFILIAL)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION)
ENGINE = InnoDB;


